package com.marcestarlet.basics

/**
 * Singleton Object
 * Objects are used to hold single instances of a class.
 * Scala has no static members.
 * Commonly used for factories.
 */
object HelloWorld {
  // main method is required and acts as the entry point of the program
  def main(args: Array[String]): Unit = {
    println("Hello World")
  }
}
